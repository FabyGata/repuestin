    {
        $sale=new Sale();
        $sale->client_name = $request->client_name;
        $sale->client_nit = $request->client_nit;
        $sale->total_price = $request->total_price;
        $sale->products_list_id = 1;
        $sale->save();
        return $this->index();
    }

